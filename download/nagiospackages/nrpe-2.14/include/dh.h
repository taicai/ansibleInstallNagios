#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xAB,0xEC,0x2F,0x2F,0x0F,0x76,0x34,0x22,0xAD,0x52,0x96,0x1E,
		0x44,0x43,0xD4,0xFC,0x6B,0xB9,0x9A,0xDE,0xB6,0xFF,0xD9,0x65,
		0xBF,0x48,0x99,0x22,0xCC,0x63,0x04,0xC5,0x16,0xBE,0x48,0x06,
		0xB8,0xFE,0x1D,0x21,0x81,0x11,0x23,0xD5,0xA9,0x79,0x25,0xAB,
		0x9F,0x22,0x62,0x9D,0x5C,0x6C,0x1F,0x6D,0x74,0x4D,0x6F,0xC8,
		0x89,0x04,0x66,0x5B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
